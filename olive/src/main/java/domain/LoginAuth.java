package domain;

public class LoginAuth {
	
	private String me_code; 
	private String me_id;
	private String me_name;
	private String mbs_code; 
	private String mbs_grade;
	
	
	public String getMe_code() {
		return me_code;
	}
	public void setMe_code(String me_code) {
		this.me_code = me_code;
	}
	
	public String getMe_id() {
		return me_id;
	}
	public void setMe_id(String me_id) {
		this.me_id = me_id;
	}
	public String getMe_name() {
		return me_name;
	}
	public void setMe_name(String me_name) {
		this.me_name = me_name;
	}
	public String getMbs_code() {
		return mbs_code;
	}
	public void setMbs_code(String mbs_code) {
		this.mbs_code = mbs_code;
	}
	public String getMbs_grade() {
		return mbs_grade;
	}
	public void setMbs_grade(String mbs_grade) {
		this.mbs_grade = mbs_grade;
	}
	

	
}
